package com.napier.sem;

/**
 * Representation of a City
 */

public class City {
    /**
     * City ID Number
     */
    public int city_no;

    /**
     * Name of City
     */
    public String name;

    /**
     * 3 Character Identification Code from country
     */
    public String country;

    /**
     * City's district
     */
    public String district;

    /**
     * Population of the City
     */
    public int population;
}

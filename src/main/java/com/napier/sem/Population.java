package com.napier.sem;

public class Population {

    /**
     * The name of the continent/region/country.
     */
    public String place;

    /**
     * The total population of the continent/region/country.
     */
    public int total;

    /**
     * The total population of the continent/region/country living in cities.
     */
    public int urban;

    /**
     * The percentage population of the continent/region/country living in cities.
     */
    public double pUrban;
    /**
     * The total population of the continent/region/country not living in cities.
     */
    public int rural;

    /**
     * The percentage population of the continent/region/country not living in cities.
     */
    public double pRural;
}

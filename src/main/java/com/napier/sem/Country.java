package com.napier.sem;

public class Country {
    /**
     * 3 Digit country ID code
     */
    public String code;

    /**
     * Name of country
     */
    public String name;

    /**
     * Continent where country is found
     */
    public String continent;

    /**
     * Region where country is found
     */
    public String region;

    /**
     * Surface area of land owned by the country
     */
    public String area;

    /**
     * Year of countries independence
     */
    public int indep_year;

    /**
     * Population of country
     */
    public int population;

    /**
     * Life expectancy of the country
     */
    public double life_expectancy;

    /**
     * Gross national product
     */
    public double gnp;

    /**
     * Gross national product (old)
     */
    public double gnpold;

    /**
     * Name for country in local language
     */
    public String local_name;

    /**
     * Type of government in country
     */
    public String government;

    /**
     * Head of state
     */
    public String head_of_state;

    /**
     * ID number for capital city
     */
    public int capital;

    /**
     * 2 digit ID code for country
     */
    public String code2;

}
